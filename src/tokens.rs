#[derive(Debug, Clone)]
#[allow(non_camel_case_types)]
pub enum TokenType {
    // SINGLE CHAR
    LEFT_PAREN,
    RIGHT_PAREN,
    LEFT_BRACE,
    RIGHT_BRACE,
    COMMA,
    DOT,
    MINUS,
    PLUS,
    SEMICOLON,
    STAR,

    // OPERATORS/MATCHES
    BANG,
    BANG_EQUAL,
    EQUAL,
    EQUAL_EQUAL,
    GREATER,
    GREATER_EQUAL,
    LESS,
    LESS_EQUAL,
    SLASH,

    // LITERAL TYPES
    IDENTIFIER,
    STRING,
    NUMBER,

    // RESERVED WORDS
    AND,
    CLASS,
    ELSE,
    FALSE,
    FOR,
    FUN,
    IF,
    NIL,
    OR,
    PRINT,
    RETURN,
    SUPER,
    THIS,
    TRUE,
    VAR,
    WHILE,

    // END OF FILE
    EOF,
}